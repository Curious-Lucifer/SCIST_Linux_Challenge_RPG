int compare_flag(char *flag){
    size_t length = strlen(flag);
    if (length != 44){
        return 0;
    }
    for (int i = 0;i < length;i++){
        if (flag[i] != (cipher[i] ^ key[i])){
            return 0;
        }
    }
    return 1;
}

